package Loop;

public class MutplicationTable {
    public static void main(String[] args) {
        // Write a program that prints the multiplication
        // table of a given number using a loop.
        int n = 5;
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " * " + i + " = " + n * i);
        }
    }
}
